#!/usr/bin/env node
/**
 * Build metadata generator
 * 
 * Writes api/commit.ts with current git commit, short commit, branch, and build time.
 * Falls back gracefully when git is unavailable or not in a repo.
 * 
 * Usage: node scripts/generate-build-meta.mjs
 */

import { execSync } from "node:child_process";
import { writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = resolve(__dirname, "..");
const outPath = resolve(root, "api", "commit.ts");

function run(cmd, fallback = null) {
  try {
    const output = execSync(cmd, { cwd: root, encoding: "utf-8", stdio: ["pipe", "pipe", "pipe"] }).trim();
    return output || fallback;
  } catch {
    return fallback;
  }
}

function firstEnv(names) {
  for (const name of names) {
    const value = process.env[name]?.trim();
    if (value) return value;
  }
  return null;
}

const commit =
  firstEnv([
    "COMMIT_SHA",
    "SOURCE_COMMIT",
    "ZBPACK_COMMIT_SHA",
    "ZEABUR_GIT_COMMIT_SHA",
    "ZEABUR_COMMIT_SHA",
    "VERCEL_GIT_COMMIT_SHA",
    "GITHUB_SHA",
    "CF_PAGES_COMMIT_SHA",
    "RAILWAY_GIT_COMMIT_SHA",
    "RENDER_GIT_COMMIT",
  ]) ?? run("git rev-parse HEAD", null);
const shortCommit = commit ? commit.slice(0, 7) : null;
const branch =
  firstEnv([
    "BRANCH",
    "GIT_BRANCH",
    "SOURCE_BRANCH",
    "ZBPACK_BRANCH",
    "ZEABUR_GIT_BRANCH",
    "ZEABUR_BRANCH",
    "VERCEL_GIT_COMMIT_REF",
    "GITHUB_REF_NAME",
    "CF_PAGES_BRANCH",
    "RAILWAY_GIT_BRANCH",
    "RENDER_GIT_BRANCH",
  ]) ?? run("git rev-parse --abbrev-ref HEAD", null);
const buildTime = firstEnv(["BUILD_TIME", "VERCEL_BUILD_TIME", "ZEABUR_DEPLOY_TIME", "DEPLOYED_AT"]) ?? new Date().toISOString();

const lines = [
  "// 此文件由构建脚本自动生成，不要手动修改",
  "// Generated by scripts/generate-build-meta.mjs",
  "",
  `export const COMMIT_SHA = ${JSON.stringify(commit ?? "unknown")};`,
  `export const SHORT_COMMIT = ${JSON.stringify(shortCommit ?? "unknown")};`,
  `export const BRANCH = ${JSON.stringify(branch ?? "unknown")};`,
  `export const BUILD_TIME = ${JSON.stringify(buildTime)};`,
  "",
  "export const BUILD_META = {",
  "  commit: COMMIT_SHA,",
  "  shortCommit: SHORT_COMMIT,",
  "  branch: BRANCH,",
  "  buildTime: BUILD_TIME,",
  "};",
  "",
];

writeFileSync(outPath, lines.join("\n"), "utf-8");
console.log(`[build-meta] Wrote ${outPath}`);
console.log(`  commit: ${commit ?? "unknown"}`);
console.log(`  short:  ${shortCommit ?? "unknown"}`);
console.log(`  branch: ${branch ?? "unknown"}`);
console.log(`  time:   ${buildTime}`);
